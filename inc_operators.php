<?php

/*
	Increment / Decrement Operators

	[ ++$var ] Pre Increment

	[ $var++ ] Post Increment

	[ --$var ] Pre Decrement

	[ $var-- ] Post Decrement
*/

$var = 10;

echo --$var . "<br>"; // 9

echo --$var . "<br>"; // 8

echo --$var . "<br>"; // 7

echo --$var . "<br>"; // 6

$var2 = 20;

echo $var2-- . "<br>";

echo $var2-- . "<br>";

echo $var2-- . "<br>";

echo $var2-- . "<br>";