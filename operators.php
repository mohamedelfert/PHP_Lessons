<?php

/*
	Arithmetic Operators

	[ + ] Addition

	[ - ] Subtraction

	[ * ] Multiplication

	[ / ] Division

	[ % ] Modulus

*/

$var1 = 2;

$var2 = 10;

echo $var1 + $var2 . "<br>";

/*
	Assignment Operators
*/

$c = 100;

$c /= 10;

echo $c . "<br>";

/*

	Comparison Operators

	[ == ] Equals

	[ != ] Not Equals

	[ <> ] Not Equals

	[ === ] Identical

	[ !== ] Not Identical

	[ < ] Less Than

	[ <= ] Less Than Or Equal

	[ > ] Larger Than

	[ >= ] Larger Than Or Equal

*/

$a = 1000;
$b = 1000;

if ($a >= $b) {

	echo "True";

} else {

	echo "False";

}